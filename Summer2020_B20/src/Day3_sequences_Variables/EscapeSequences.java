package Day3_sequences_Variables;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello Everyone \nMy name is Cybertek");
        System.out.println("Hello \nMy name is beyza \n\tI am in el paso \n\n\t\tI like Java");

        System.out.println("\\");    //this prints me one backslash
        System.out.println("/");     //this prints me one front slash
        System.out.println("//");    // this prints me two front slash
        System.out.println("\\\\");  //this prints me two backslash

        System.out.println("My name is \'Beyza\'"); //it prints me 'Beyza'
        System.out.println("My name is 'Beyza'"); //it also prints me 'Beyza'
        System.out.println("My favorite tv show is \"Lost\"");
    }


}
