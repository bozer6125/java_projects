package day07_Unary_ShortHand;

public class Unary {
    public static void main(String[] args) {
        int x = 100;
        System.out.println(++x); //101
        int y = 200;
        System.out.println(--y);//199
        int z = 80;
        System.out.println(--z);//79



        System.out.println(-1+-10);
    }
}
